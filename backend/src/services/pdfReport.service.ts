      this.log(`üìÑ [PDF REPORT] –ù–∞–π–¥–µ–Ω —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π PDF –æ—Ç—á–µ—Ç: ${existingPdfPath}`);
